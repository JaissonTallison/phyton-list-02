# Verificar se um número está em um intervalo. Receba um número e exiba se ele está entre 10 e 50 (inclusive).
print('=============================================================')
num = int(input('Digite um número: '))
print('=============================================================')

if (num >= 10 and num <= 50):
    print(f'O número {num} esta entre 10 e 50: ')
else:
    print(f'O número {num} nã0 esta entre 10 e 50: ')
print('=============================================================')
