# Verificar se um número é par ou ímpar
# Peça ao usuário para digitar um número
# inteiro e exiba se ele é par ou ímpar
print('=============================================================')
num = int(input('Digite um número: '))
print('=============================================================')

if (num % 2 == 0):
    print('NÚMERO PAR!')
else:
    print('NÚMERO IMPAR!')
print('=============================================================')

